
public class Triangle
{
	private double sideA;
	private double sideB;
	private double sideC;
	
	Triangle(double sideA, double sideB, double sideC)
	{
		this.sideA = sideA;
		this.sideB = sideB;
		this.sideC = sideC;
	}
	
	public double getSideA()
	{
		return this.sideA;
	}
	
	public double getSideB()
	{
		return this.sideB;
	}
	
	public double getSideC()
	{
		return this.sideC;
	}
}
